package model;

public class Parameter {
	
	private String name;
	private String datatype;
	private String type;
	private Object value;
	private String importFromFile;
	private String xlFileRange;
	private String randomization;
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the datatype
	 */
	public String getDatatype() {
		return datatype;
	}
	/**
	 * @param datatype the datatype to set
	 */
	public void setDatatype(String datatype) {
		this.datatype = datatype;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the value
	 */
	public Object getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(Object value) {
		this.value = value;
	}
	/**
	 * @return the importFromFile
	 */
	public String getImportFromFile() {
		return importFromFile;
	}
	/**
	 * @param importFromFile the importFromFile to set
	 */
	public void setImportFromFile(String importFromFile) {
		this.importFromFile = importFromFile;
	}
	/**
	 * @return the xlFileRange
	 */
	public String getXlFileRange() {
		return xlFileRange;
	}
	/**
	 * @param xlFileRange the xlFileRange to set
	 */
	public void setXlFileRange(String xlFileRange) {
		this.xlFileRange = xlFileRange;
	}
	/**
	 * @return the randomization
	 */
	public String getRandomization() {
		return randomization;
	}
	/**
	 * @param randomization the randomization to set
	 */
	public void setRandomization(String randomization) {
		this.randomization = randomization;
	}
	
	
	

}
